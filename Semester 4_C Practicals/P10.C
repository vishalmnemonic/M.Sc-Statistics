#include<stdio.h>
#include<conio.h>
#include<math.h>
#include<stdlib.h>

FILE *p, *q;

void main(){
	int i, n=9;
	float l[10],L[10],T[10],S,exo[10],d[10],qx[10];
	clrscr();
	p=fopen("C:/Data/cwork/in10.txt","r");
	q=fopen("C:/Data/cwork/out10.txt","w");
	for(i=0; i<n; i++)
		fscanf(p, "%f", &qx[i]);
	l[0]=1000;
	for(i=0; i<n;i++){
		d[i]=qx[i]*l[i];
		l[i+1]=l[i]-d[i];
		L[i]=l[i]-d[i]*0.5;
		S +=L[i]; 
	}
	T[0]=S;
	for(i=1; i<=n; i++){
		T[i]=T[i-1] - L[i-1];
		exo[i-1]=T[i-1]/l[i-1];
	}
	fprintf(q, "x\tqx\t\tdx\tlx\tLx\t\tTx\t\texo\n");
	for(i=0;i<n;i++)
		fprintf(q, "%d\t%f\t%.0f\t%.0f\t%f\t%f\t%f\n", i, qx[i], d[i],l[i],L[i],T[i],exo[i]);
	fclose(p);
	fclose(q);
}

