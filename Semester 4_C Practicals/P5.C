#include<stdio.h>
#include<math.h>
#include<stdlib.h>
void main(){
	int i,n,k=0;
    float x[15],a,s[15][15],j,e[15][15],m[15]={0.0};
    FILE *p,*q;
    p=fopen("C:/Data/cwork/in5.txt","r");
	q=fopen("C:/Data/cwork/out5.txt","w");
    fscanf(p,"%d%f",&n,&a);
    for(i=0;i<n;i++)
        fscanf(p,"%f",&x[i]);
    for(j=a;j<=0.5;j+=0.1){
		for(i=1;i<n;i++){
			s[0][k]=x[0];
	       s[i][k]=j*x[i]+(1-j)*s[i-1][k];
	       e[i][k]=pow(x[i]-s[i][k],2);
	       m[k]+=e[i][k];
		}
		m[k]/=n-1;
		k++;
    }
	fprintf(q,"Days\tXt\t");
	for(j=a;j<=0.5;j+=0.1)
		fprintf(q,"S[%.1f]\t",j);
	fprintf(q,"\n");
	for(i=0;i<n;i++){
		fprintf(q,"%d\t%.0f\t",i+1,x[i]);
		for(k=0;k<5;k++)
			fprintf(q," %0.2f\t",s[i][k]);
		fprintf(q,"\n");        
	}
	fprintf(q,"\nMSE\t\t");
	for(k=0;k<5;k++)
	fprintf(q," %.2f\t",m[k]);
	fclose(p);
	fclose(q);
}
